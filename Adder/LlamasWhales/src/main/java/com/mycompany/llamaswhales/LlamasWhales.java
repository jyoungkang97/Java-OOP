public class LlamasWhales {
    public static void main(String[] args) {
        int llamas = 20;
        int whales = 15;
        int dodos = 0;
    
    if (dodos > 0); {
        System.out.println("I thought dodo's were extinct!");
    }
    
    if (dodos < 0); {
        System.out.println("Hold on, how can we have negative Dodos?");
    }
    
    if (llamas > whales); {
        System.out.println("Whales may be bigger, but we have more Llamas!");
    }
    
    if (llamas <= whales); {
        System.out.println("Qualiity over quantity, Whales are better.");
    }
    
        System.out.println("Dodo's have been cloned, and are no longer extinct!");
        
        dodos += 100;
    
    if ((whales + llamas) < dodos) {
        System.out.println("I never thought I'd see Dodo's rule the Earth.");
    }
    
    if (llamas > whales && llamas > dodos) {
        System.out.println("Llamas come out ahead, sneaky!");
    }
  }
}
